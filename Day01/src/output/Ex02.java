package output;

public class Ex02 {
	public static void main(String[] args) {
		// print 구문은 데이터를 콘솔에 출력하는 함수
		
		// #문자열?
		// - " "로 묶인 데이터를 의미
		// - 단어나 문장을 표현하기 위한 타입
		
		System.out.println("문자열을 출력~\n");
		
		System.out.println("10");
		System.out.println(10);
		System.out.println(10.0);
		
		System.out.println();
		
		System.out.println("10" + "20");
		System.out.println(10 + 20);
	}
}
