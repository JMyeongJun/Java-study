package variable;

public class Ex01 {
	public static void main(String[] args) {
		// 변수 : 데이터를 저장하는 공간. 변하는 수
		
		int num = 10;
		
		System.out.println("num");
		System.out.println(num);
		
		System.out.println("\nnum = " + num + "\n");
		
		num = 20;
		
		System.out.println("num = " + num);
	}
}
